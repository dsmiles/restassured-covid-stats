package com.github.dsmiles.paths;

/**
 * Utility class providing paths for various endpoints of the Corona API.
 */
public class CoronaPaths {

    public String countriesData() {
        return "/countriesData";
    }

    public String totalData() {
        return "/totalData";
    }

    public String coronaNews() {
        return "/coronaNews";
    }

    public String continentData() {
        return "/continentData";
    }
}
